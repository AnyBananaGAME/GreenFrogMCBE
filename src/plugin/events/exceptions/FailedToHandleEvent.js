const { lang } = require("../../../server/ServerInfo");
const Logger = require("../../../server/Logger");

module.exports = {
  handleEventError(e, plugin, name) {
    if (
      e.toString().startsWith(`TypeError: require(...)`) &&
      e.toString().includes("is not a function")
    )
      return;
    Logger.log(
      lang.errors.failedToExecuteEvent
        .replace("%plugin%", plugin)
        .replace("%event%", name)
        .replace("%e%", e.stack),
      "error"
    );
  },
};
