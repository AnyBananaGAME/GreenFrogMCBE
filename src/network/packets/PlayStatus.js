let status = "";

class PlayStatus extends require("./Packet") {
	/**
	 * @returns The name of the packet.
	 */
	name() {
		return "play_status";
	}

	/**
	 * It sets the status.
	 * @param status1 - The status
	 */
	setStatus(status1) {
		status = status1;
	}

	/**
	 * It returns the status.
	 * @returns The status
	 */
	getStatus() {
		return status;
	}

	send(client) {
		client.queue(this.name(), {
			status: this.getStatus(),
		});
	}
}

module.exports = PlayStatus;
