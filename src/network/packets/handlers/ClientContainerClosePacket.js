const ContainerClose = require("../ContainerClose");
const Handler = require("./Handler");

class ClientContainerClose extends Handler {
	handle(client) {
		const co = new ContainerClose();
		co.setServer(false);
		co.setWindowId(-1);
		co.send(client, 3);
	}
}

