const Event = require("./Event");

class PlayerResourcePacksCompletedEvent extends Event {
	constructor() {
		super();
		this.cancelled = false;
		this.name = "PlayerResourcePacksCompletedEvent";
		this.player = null;
		this.server = null;
	}

	cancel() {
		this.player.kick();
		this.cancelled = true;
	}

	execute() {
		this._execute(this)();
	}
}

