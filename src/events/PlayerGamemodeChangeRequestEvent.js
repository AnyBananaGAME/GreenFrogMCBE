const Event = require("./Event");

class PlayerGamemodeChangeRequestEvent extends Event {
    constructor() {
        super();
        this.cancelled = false;
        this.name = "PlayerGamemodeChangeRequestEvent";
        this.server = null;
        this.player = null;
        this.gamemode = null;
    }

    cancel() {
        this.cancelled = true;
    }

    async execute() {
        if (!this.cancelled) {
            player.setGamemode(this.gamemode);
        }
    }
}

module.exports = PlayerGamemodeChangeRequestEvent;