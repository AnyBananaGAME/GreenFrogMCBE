const Event = require("./Event");

class PlayerSpawnEvent extends Event {
	constructor() {
		super();
		this.cancelled = false;
		this.name = "PlayerSpawnEvent";
		this.player = null;
		this.server = null;
	}

	cancel(client) {
		client.kick();
		this.cancelled = true;
	}

	execute() {
		this._execute(this)();
	}
}

module.exports = PlayerSpawnEvent;
