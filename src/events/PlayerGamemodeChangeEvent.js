const FailedToHandleEvent = require("./exceptions/FailedToHandleEvent");
const Event = require("./Event");

const fs = require("fs");

class PlayerGamemodeChangeEvent extends Event {
	constructor() {
		super();
		this.cancelled = false;
		this.name = "PlayerGamemodeChangeEvent";
		this.server = null
		this.player = null
		this.gamemode = null
		this.oldgamemode = null
	}

	cancel() {
		this.cancelled = true;
	}

	async execute() {
		await this._execute()

		if (!this.cancelled) {
			this.player.oldgamemode = this.gamemode;
			return
		}
		
		this.player.setGamemode(this.oldgamemode);
	}
}

module.exports = PlayerGamemodeChangeEvent;
