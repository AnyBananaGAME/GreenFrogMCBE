const Text = require("../network/packets/ServerTextPacket");
const Event = require("./Event");

class ServerToClientChatEvent extends Event {
	constructor() {
		super();
		this.cancelled = false;
		this.name = "ServerToClientChatEvent";
		this.server = null;
		this.player = null;
		this.message = null;
	}

	cancel() {
		this.cancelled = true;
	}

	async execute() {
		await this._execute(this);

		if (!this.cancelled) {
			const text = new Text();
			text.setMessage(this.message);
			text.writePacket(this.player);
		}
	}
}

module.exports = ServerToClientChatEvent;
